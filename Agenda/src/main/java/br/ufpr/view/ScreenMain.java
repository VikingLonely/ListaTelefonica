    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        if (jTable2.getSelectedRow() != -1) {
            Agenda ag = new Agenda();
            AgendaDAO dao = new AgendaDAO();
            ag.setId(Integer.parseInt(jfId.getText()));
            ag.setNome(jtNome.getText());
            ag.setSobrenome(jtSobrenome.getText());
            ag.setTelefone(jfTelefone.getText());
            int resultado = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir esse registro?", "Excluir Registro",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (resultado == 0) {
                dao.excluir(ag);
            }
            limparTela();
            readTabela();
        } 
        jbSalvar.setEnabled(true);
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:

        if (jfId.getText().isEmpty()) {
            Agenda ag = verificarCampos();
            AgendaDAO dao = new AgendaDAO();
            dao.criar(ag);
            limparTela();
            readTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Impossivel registrar usuarios duplicados!");
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

