    public void limparTela() {
        jfId.setText("");
        jtNome.setText("");
        jtSobrenome.setText("");
        jfTelefone.setText("");
        jbSalvar.setEnabled(true);
    }

    public void selecionarLinha() {
        if (jTable2.getSelectedRow() != -1) {
            jfId.setText(jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString());
            jtNome.setText(jTable2.getValueAt(jTable2.getSelectedRow(), 1).toString());
            jtSobrenome.setText(jTable2.getValueAt(jTable2.getSelectedRow(), 2).toString());
            jfTelefone.setText(jTable2.getValueAt(jTable2.getSelectedRow(), 3).toString());
        }
        jbSalvar.setEnabled(false);
    }

    public void readTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setNumRows(0);
        AgendaDAO dao = new AgendaDAO();

        for (Agenda a : dao.read()) {
            model.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getSobrenome(),
                a.getTelefone()
            });
        }
    }

    /**
     *
     * @return
     */
    public Agenda verificarCampos() {
        Agenda ag = new Agenda();
        if (jtNome.getText().isEmpty() || jtSobrenome.getText().isEmpty() || jfTelefone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor! Preencha todos os campos!");
            return null;
        } else {
            ag.setNome(jtNome.getText());
            ag.setSobrenome(jtSobrenome.getText());
            ag.setTelefone(jfTelefone.getText());
        }
        return ag;
    }
    
    public Agenda verificarCamposAlterar(){
        Agenda ag = new Agenda();
        if (jtNome.getText().isEmpty() || jtSobrenome.getText().isEmpty() || jfTelefone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor! Preencha todos os campos!");
        } else {
            ag.setId(Integer.parseInt(jfId.getText()));
            ag.setNome(jtNome.getText());
            ag.setSobrenome(jtSobrenome.getText());
            ag.setTelefone(jfTelefone.getText());
        }
        return ag;
    }

